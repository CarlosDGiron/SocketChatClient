    private void createUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserBtnActionPerformed
        Request registryRequest;
        Response registryResponse;
        if (Arrays.equals(password.getPassword(),passwordConfirm.getPassword())) {
            registryUser = new User(0, userName.getText(), password.getText());
            registryRequest = new Request(requestType, registryUser);
            registryResponse = register(registryRequest);
            boolean registrySuccesful=registryResponse.getResponseValue();
            if (registrySuccesful) {
                JOptionPane.showMessageDialog(this, "El usuario se ha creado exitosamente.", "Usuario creado",  JOptionPane.INFORMATION_MESSAGE);               
                clearTextFields();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "El usuario ingresado ya existe.", "Usuario ya existe",  JOptionPane.ERROR_MESSAGE);
                userName.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Las contrase単as ingresadas no coinciden.", "Contrase単as no coinciden", JOptionPane.ERROR_MESSAGE);
            password.setText("");
            passwordConfirm.setText("");
        }
    }//GEN-LAST:event_createUserBtnActionPerformed
    private void clearTextFields() {
        userName.setText("");
        password.setText("");
        passwordConfirm.setText("");
    }

    private Response register(Request registryRequest) {        
        try {
            Response registryResponse;
            Socket socketClient = new Socket(HOST, 5050);
            InputStream inputStream = socketClient.getInputStream();
            ObjectInputStream socketResponse = new ObjectInputStream(inputStream);
            OutputStream outputStream = socketClient.getOutputStream();
            ObjectOutputStream socketRequest = new ObjectOutputStream(outputStream);
            socketRequest.writeObject(registryRequest);
            registryResponse = (Response) socketResponse.readObject();
            socketClient.close();            
        return registryResponse;
        } catch (Exception e) {
            System.out.println(e.getMessage() + " - " + e.getCause());
            return null;
        }
    }

